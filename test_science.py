LONG_TEXT = """2015年9月14日，人类首次直接探测到引力波。这一由两颗黑洞合并产生的引力波信号被命名为GW150914，由美国激光干涉仪引力波天文台（LIGO）的两个探测器分别在路易斯安那州利文斯顿和华盛顿州汉福德同时捕获。这一发现标志着引力波天文学新时代的开启，也证实了爱因斯坦1916年根据广义相对论做出的预言。GW150914探测到的引力波来自13亿光年外两个质量分别为36太阳质量和29太阳质量的黑洞合并，合并后形成了一个62太阳质量的黑洞。GW150914的信噪比高达24，是一次极为"响亮"的探测。

在GW150914之后，LIGO与位于意大利的室女座引力波探测器（Virgo）、日本的神冈引力波探测器（KAGRA）联合组成LVK合作组（LIGO-Virgo-KAGRA Collaboration），持续开展观测。到目前为止，这些探测器在第四次观测运行中已经观测到超过200次黑洞合并事件，自2015年第一次观测运行开始以来总共观测到约300次黑洞合并事件。

2023年11月23日13时54分30秒（UTC），LVK合作组探测到一次特殊的引力波事件，被命名为GW231123。这是迄今为止通过引力波观测到的最巨大的黑洞合并事件——合并产生的最终黑洞质量约为太阳质量的225倍。GW231123在LIGO第四次观测运行（O4a，2023年5月24日至2024年1月16日）期间被探测到。GW231123信号由LIGO汉福德探测器和利文斯顿探测器同时探测到，这对做出可靠的探测至关重要。信号包含约5个周期，频率范围为30-80赫兹。

天文学家将GW231123解释为双黑洞合并，并推断总质量在190到265太阳质量之间，两个黑洞的旋转速度都非常高（自旋参数分别约为0.9和0.8）。虽然此前已有一些引力波候选体显示出类似的高总质量，但它们的误警率都不低于每年一次；而GW231123不仅具有很高的信噪比，还具有很高的统计显著性。如此高的质量和自旋对现有的精确波形模型构成了挑战，导致在黑洞质量、双星方向和距离等方面的测量不确定性比此前同等强度的任何信号都大。

按照天体物理学家的理解，恒星坍缩形成黑洞时，由于不稳定性超新星（pair instability supernovae）和脉动式不稳定性超新星（pulsational pair instability supernovae）的存在，恒星无法直接坍缩为质量在60-130太阳质量之间的黑洞（这一质量区间被称为"质量间隙"或"对不稳定质量间隙"）。从引力波目录中推断出的大多数黑洞都低于这个间隙。GW231123测量上的大不确定性意味着，初始质量较大的黑洞可能在质量间隙之内或之外，而较轻的那颗黑洞的质量范围可能横跨整个间隙。两种黑洞质量分别位于间隙两侧的情况也是可能的。最可能的形成场景需要某种能够填补质量间隙的机制，例如先前的恒星合并或黑洞合并。

GW231123事件中黑洞的高自旋速度接近爱因斯坦广义相对论所允许的极限，这使得信号难以建模和解释。这为推动理论工具的发展提供了一个优秀的研究案例。LVK研究人员表示，学术界可能需要数年时间才能完全解开这个复杂信号模式及其所有影响。尽管最可能的解释仍然是黑洞合并，但不能排除更复杂的场景，这些场景可能是解读其意外特征的关键。

2021年探测到的GW190521事件此前是记录中最大质量的黑洞合并，总质量达到太阳的140倍。GW231123则刷新了这一纪录，由两颗分别约为100倍和140倍太阳质量的黑洞合并产生。

在GW231123前后，LVK合作组还探测到了多个其他重要事件。2023年5月29日探测到的GW230529是一个特殊事件，较大质量的主星被估计拥有3.6倍太阳质量的物体。该物体质量大于中子星的预期质量上限，同时又小于黑洞的预期质量下限，使得科学家难以确认它的具体分类。这一质量范围被称为"低质量间隙"，指的是人们预期将极少探测到质量在3至5倍太阳质量之间的致密天体。基于对核物理理论与实验以及天体物理中的中子星族群的现有认知，主星是中子星的可能性被排除（具有99%的概率），是一颗质量小于5倍太阳质量的黑洞。GW230529是首例主星位于低质量间隙的双星候选体事件。

GW230529是使用匹配滤波技术分析探测器数据时被探测到的。该技术将探测数据与理论给出的预期信号模板相比较，找出匹配程度最佳的潜在真实引力波信号。人们据此便能够估计出随时间变化的引力波信号强度。如果在引力波探测器的数据中存在来自真实天体物理源的引力波信号，信噪比数值将会非常高；反之则会很低。GW230529在仅有LIGO Livingston探测器能够提供可用数据的情况下被成功探测，三个独立的搜寻算法报告了探测结果，误报率为每千年低于一例。

2023年8月14日，LVK合作组使用LIGO Livingston探测器探测到一次特别响亮的引力波信号GW230814。该信号最有可能来自两个黑洞的合并，单探测器信噪比约为42。GW230814被认为来自两个质量分别为太阳34倍和28倍的黑洞合并，与首次引力波探测GW150914惊人地相似。这一宇宙事件发生在距离地球约3亿秒差距（约10亿光年）的地方。

GW230814的信号被用来检验爱因斯坦的广义相对论。引力波信号通常包括三个阶段：旋进（inspiral，两个黑洞相互靠近）、合并-骤降（plunge-merger）和铃宕（ringdown，新生黑洞通过发射引力波逐渐稳定）。对GW230814旋进阶段的检验表明信号与广义相对论一致。然而，聚焦于合并-骤降-铃宕阶段的检验描绘出更加令人费解的画面：GW230814的铃宕比广义相对论预期消退得更快。

在2025年9月10日出版的《物理评论快报》杂志上，LVK合作组报告了利用引力波信号GW250114以99.999%的置信度验证了斯蒂芬·霍金1971年提出的黑洞面积定理。这一成果为量子引力理论的研究奠定了重要基础。黑洞面积定理指出，合并后新生黑洞的事件视界（即连光也无法逃脱的边界）总面积，不小于原先两个黑洞事件视界面积之和。升级后的LIGO探测器灵敏度已达到2015年时的三倍，能够以更高精度捕捉更多黑洞碰撞事件。GW250114源自约13亿光年外、两个质量可能为太阳32倍左右的黑洞的碰撞。合并前后，事件视界总面积分别为24万平方公里和40万平方公里。GW250114是迄今探测到的"最响亮"的引力波事件，使LVK团队有机会清晰验证霍金的预言。以往这些"泛音"消散太快，难以捕捉并用于计算视界面积；2021年曾有研究以95%的置信度支持该定理，而本次结果将置信度大幅提升至99.999%。

此外，借助GW250114的数据，科学家也证实了数学家罗伊·克尔于20世纪60年代提出的描述黑洞的方程：即只需质量和自旋两个参数，就可完全确定一个黑洞。具有相同质量和自旋的黑洞，在数学上是完全相同的。

LIGO探测器是测量由剧烈宇宙事件引起的时空微小扭曲的精密仪器。第四次观测运行于2023年5月开始，更多观测结果将在后续发表。引力波天文学为我们揭示了黑洞在整个宇宙中的基本和奇异性质，未来还有更多有待发现的奥秘。"""

