def call_v4(system_prompt, user_content, model=MODEL):
    from openai import OpenAI
    client = OpenAI(api_key=API_KEY, base_url=BASE_URL)
    response = client.chat.completions.create(
        model=model,
        messages=[
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_content}
        ],
        temperature=TEMPERATURE,
        max_tokens=MAX_TOKENS
    )
    return {
        "answer": response.choices[0].message.content,
        "usage": {
            "prompt_tokens": response.usage.prompt_tokens,
            "completion_tokens": response.usage.completion_tokens,
            "total_tokens": response.usage.total_tokens
        }
    }

def evaluate_answer(answer, expected):
    keywords = [kw.strip() for kw in expected.replace("，", ",").replace("、", ",").split(",") if kw.strip()]
    hits = sum(1 for kw in keywords if kw in answer)
    return {"keywords": keywords, "hits": hits, "total": len(keywords), "score": hits / max(len(keywords), 1)}


