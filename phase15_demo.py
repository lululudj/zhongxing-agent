COMPRESS_SYSTEM = """你是一个文档压缩引擎。你的任务是将长文档压缩为三层结构化摘要。

## 最高优先级规则（违反任何一条都是严重错误）
1. **数值绝对保护**：原文中出现的所有具体数字（百分比、金额、倍数、日期、比率等）必须精确保留在L1或L2中。宁可超字数，不可丢数字。
2. **实体不遗漏**：原文中出现的所有专有名词（人名、机构名、事件名、指标名）必须在L2中列出。
3. **因果关系保留**：原文中的因果链条（A导致B）必须在L2的关系字段中体现。

## 输出格式（严格遵循）

## L1 全局摘要
（3-5句核心结论，每句必须包含至少一个具体数值）

## L2 实体与关系
（每个实体一行，格式：实体名 | 属性值(逗号分隔，必须保留原始数值) | 关系(逗号分隔)）

## L3 段落索引
（每段一行，格式：段号 | 首句缩写 | 关键实体(逗号分隔)）

## 字数指引（数值保护优先于字数限制）
- L1：200字以内（如需保留数值可超出）
- L2：每个实体60字以内（数值必须完整）
- L3：每段30字以内"""

COMPRESS_USER = """请将以下文档压缩为三层摘要：

{text}"""


def compress_to_three_layer(text, name=""):
    """用V4-Flash将原文压缩为三层摘要"""
    from openai import OpenAI
    client = OpenAI(api_key=API_KEY, base_url=BASE_URL)
    
    print(f"  压缩 {name} ({len(text)}字)...", end=" ", flush=True)
    start = time.time()
    
    response = client.chat.completions.create(
        model=COMPRESS_MODEL,
        messages=[
            {"role": "system", "content": COMPRESS_SYSTEM},
            {"role": "user", "content": COMPRESS_USER.format(text=text)}
        ],
        temperature=0.1,  # 低温度，追求精确压缩
        max_tokens=4096
    )
    
    summary = response.choices[0].message.content
    usage = response.usage
    elapsed = time.time() - start
    
    print(f"✅ {elapsed:.1f}s, 输入{usage.prompt_tokens}tok → 输出{usage.completion_tokens}tok")
    
    return {
        "summary": summary,
        "usage": {"prompt_tokens": usage.prompt_tokens, "completion_tokens": usage.completion_tokens, "total_tokens": usage.total_tokens},
        "time": elapsed
    }


